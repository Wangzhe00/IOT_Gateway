#include  "include.h"

/*
  默认的外部时钟频率7.3728MHZ
*/
#define CH438UART5_BPS  9600            /* 定义CH438串口0通讯波特率 */
#define Fpclk           2666666         /* 定义内部时钟频率    */



/*********************************************************************************************************
** 函数名称: InitCH438UART5
** 功能描述: 主函数调用，用于初始化CH438串口5
** 输　入: 无
**
** 输　出: 无
**         
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/

void InitCH438UART5( void )       
{
    UINT16 div;
    UINT8 DLL, DLM;
/**************************************************************************
          设置CH438串口5的寄存器
**************************************************************************/
    UART5_Reset();
    uDelay(50);
    div = ( Fpclk >> 4 ) / CH438UART5_BPS;
    DLM = div >> 8;
    DLL = div & 0xff;
    WriteCH438Data( REG_LCR5_ADDR, BIT_LCR_DLAB );    /* 设置DLAB为1 */
    WriteCH438Data( REG_DLL5_ADDR, DLL );             /* 设置波特率 */
    WriteCH438Data( REG_DLM5_ADDR, DLM );

    WriteCH438Data( REG_FCR5_ADDR, BIT_FCR_RECVTG1 | BIT_FCR_RECVTG0 | BIT_FCR_FIFOEN );    /* 设置FIFO模式，触发点为112字节 */

    WriteCH438Data( REG_LCR5_ADDR, BIT_LCR_WORDSZ1 | BIT_LCR_WORDSZ0 );                     /* 字长8位，1位停止位、无校验 */

    WriteCH438Data( REG_IER5_ADDR, /*BIT_IER_IEMODEM |*/ BIT_IER_IELINES | BIT_IER_IETHRE | BIT_IER_IERECV );    /* 使能中断 */

    WriteCH438Data( REG_MCR5_ADDR, BIT_MCR_OUT2    | BIT_MCR_RTS     | BIT_MCR_DTR );              /* 允许中断输出,DTR,RTS为1 */
		
    WriteCH438Data( REG_MCR1_ADDR, 0);              /* 串口1DTR为0，485做发送器*/

    WriteCH438Data(REG_FCR5_ADDR,ReadCH438Data(REG_FCR5_ADDR)| BIT_FCR_TFIFORST|BIT_FCR_RFIFORST|BIT_FCR_FIFOEN);
    uDelay(20);
}



/*********************************************************************************************************
** 函数名称: UART5_Reset
** 功能描述: 功能函数，用于复位CH438串口5, 复位之后需要重新初始化串口 才可以正常使用
** 输　入: 无
**
** 输　出: 无
**         
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void UART5_Reset( void )
{

    WriteCH438Data( REG_IER5_ADDR, BIT_IER_RESET ); 

}




