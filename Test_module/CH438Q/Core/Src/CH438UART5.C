void Auto_HandFlow5_Ctrl( void )
{

    WriteCH438Data( REG_MCR5_ADDR, BIT_MCR_AFE | BIT_MCR_OUT2 | BIT_MCR_RTS );    /* 设置MCR寄存器的AFE和RTS为1 */

}


