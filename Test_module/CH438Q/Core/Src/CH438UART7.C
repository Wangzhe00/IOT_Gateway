/*********************************************************************************************************
** 函数名称: UART7_SendByte
** 功能描述: 功能函数，用于CH438串口7 发送单个字节数据
** 输　入: 发送数据
**
** 输　出: 无
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void UART7_SendByte( UINT8 dat )    
{
  
    while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_THRE ) == 0 );    /* 等待数据发送完毕 */

    WriteCH438Data( REG_THR7_ADDR, dat );

}




/*********************************************************************************************************
** 函数名称: UART7_RcvByte
** 功能描述: 功能函数，用于CH438串口7 接收单个字节数据
** 输　入: 无
**
** 输　出: 接收数据
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
UINT8 UART7_RcvByte( void )    
{

    UINT8 Rcvdat = 0x00;
    volatile uint8_t recv7 = ReadCH438Data( REG_LSR7_ADDR );
    if( !( recv7 & ( BIT_LSR_BREAKINT | BIT_LSR_FRAMEERR | BIT_LSR_PARERR | BIT_LSR_OVERR ) ) )    /*  b1-b4无错误 */
    {

        while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_DATARDY ) == 0 ) {
             recv7 = ReadCH438Data( REG_LSR7_ADDR );
        };    /* 等待数据准备好 */

        Rcvdat = ReadCH438Data( REG_RBR7_ADDR );    /* 从接收缓冲寄存器读出数据 */  
    }

    else {
        ReadCH438Data( REG_RBR7_ADDR );
        }    /* 有错误清除 */
	return Rcvdat;
}





/*********************************************************************************************************
** 函数名称: UART7_SendStr
** 功能描述: 功能函数，用于CH438串口7 发送一个字符串
** 输　入: 字符串
**
** 输　出: 无
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void UART7_SendStr( UINT8 *str )    
{

    while( 1 )
    {

        if( *str == '\0' ) break;

        UART7_SendByte( *str++ );

    }

}


/*********************************************************************************************************
** 函数名称: CH438Seril7Send
** 功能描述: 功能函数，禁用FIFO模式, 用于CH438串口7 发送多字节数据
** 输　入: 数据块
**
** 输　出: 无
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void  CH438Seril7Send( UINT8 *Data, UINT8 Num )
{
    do
    {

        while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_THRE ) == 0 );    /* 等待数据发送完毕 */

        WriteCH438Data( REG_THR7_ADDR, *Data++ );

    }

    while( --Num );

}



/*********************************************************************************************************
** 函数名称: CH438Seril7Rcv
** 功能描述: 功能函数，禁用FIFO模式, 用于CH438串口7 接收多字节数据
** 输　入: 接收数据缓冲区地址
**
** 输　出: 接收数据长度
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/

UINT8  CH438Seril7Rcv( UINT8 *buf )
{
    UINT8 RcvNum = 0;

    if( !( ReadCH438Data( REG_LSR7_ADDR ) & ( BIT_LSR_BREAKINT | BIT_LSR_FRAMEERR | BIT_LSR_PARERR | BIT_LSR_OVERR ) ) )    /* b1-b4无错误 */
    {

        while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_DATARDY ) == 0 );    /* 等待数据准备好 */

        do
        {

            *buf++ = ReadCH438Data( REG_RBR7_ADDR );                           /* 从接收缓冲寄存器读出数据 */

            RcvNum++;

        }

        while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_DATARDY ) == 0x01 );
									   
    }

    else ReadCH438Data( REG_RBR7_ADDR );

    return( RcvNum );
}








/*********************************************************************************************************
** 函数名称: CH438UART7Send
** 功能描述: 功能函数，启用FIFO模式, 用于CH438串口7 发送多字节数据 单次最多发送128字节数据
** 输　入: 发送数据缓冲区地址, 发送数据块长度
**
** 输　出: 无
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void  CH438UART7Send( UINT8 *Data, UINT8 Num )
{

    while( 1 )
    {

        while( ( ReadCH438Data( REG_LSR7_ADDR ) & BIT_LSR_TEMT ) == 0 );    /* 等待数据发送完毕，THR,TSR全空 */

        if( Num <= 128 )
        {

            WriteCH438Block( REG_THR7_ADDR, Num, Data );

            break;

        }

        else
        {

            WriteCH438Block( REG_THR7_ADDR, 128, Data );

            Num -= 128;

            Data += 128;

        }

    }
}



/*********************************************************************************************************
** 函数名称: Auto_HandFlow7_Ctrl
** 功能描述: 功能函数，串口7自动硬件流控制
** 输　入: 无
**
** 输　出: 无
**
** 日　期: 2011年8月26日
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
**-------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void Auto_HandFlow7_Ctrl( void )
{

    WriteCH438Data( REG_MCR7_ADDR, BIT_MCR_AFE | BIT_MCR_OUT2 | BIT_MCR_RTS );    /* 设置MCR寄存器的AFE和RTS为1 */

}


